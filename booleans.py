# --------------
# Comparison Operators
a = 14
b = 5

# equals
a == b

# doesn't equal
a != b

# greater than
a > b

# greater than or equal to
a >= b

# less than
a < b

# less than or equal to
a <= b

# equality with strings
a = '1 2 3'
b = 'one two 3'
b == b
a != b

# --------------
# logic operators (compound boolean expressions)

# `and`
temperature = 28
sunny = True
temperature > 25 and sunny
sunny = False
temperature > 25 and sunny

# `or`
has_coffee = True
has_beer = True
has_coffee or has_beer
has_coffee = False
has_coffee or has_beer
